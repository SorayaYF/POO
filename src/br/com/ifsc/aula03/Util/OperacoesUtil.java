package br.com.ifsc.aula03.Util;

public class OperacoesUtil {
    //Exemplo de métodos estáticos
    public static float multiplicar(float primeiroValor, float segundoValor) {
        return primeiroValor * segundoValor;
    }

    public static float dividir(float primeiroValor, float segundoValor) {
        return primeiroValor / segundoValor;
    }

    public static float subtrair(float primeiroValor, float segundoValor) {
        return primeiroValor - segundoValor;
    }

    public static float somar(float primeiroValor, float segundoValor) {
        return primeiroValor + segundoValor;
    }
}
